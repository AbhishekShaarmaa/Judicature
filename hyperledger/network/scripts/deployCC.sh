if [ -z "$1" ]; then
    echo "Usage: ./deployChaincode.sh -lang <language> -v <version> -src <path> -name <name>"
    exit 1
fi

. initVars.sh cc "$@"

if [ -z "$CC_RUNTIME_LANGUAGE" ]; then
    echo "CC_RUNTIME_LANGUAGE is not set"
    exit 1
elif [ -z "$VERSION" ]; then
    echo "VERSION is not set"
    exit 1
elif [ -z "$CC_SRC_PATH" ]; then
    echo "CC_SRC_PATH is not set"
    exit 1
elif [ -z "$CC_NAME" ]; then
    echo "CC_NAME is not set"
    exit 1
fi

presetup() {
    echo "Installing dependencies..."
    pushd $CC_SRC_PATH
    yarn
    popd
    echo Finished installing dependencies
}

packageChaincode() {
    rm -rf ${CC_NAME}.tar.gz
    setGlobalsForPeer0Org1
    peer lifecycle chaincode package ${CC_NAME}.tar.gz \
        --path ${CC_SRC_PATH} --lang ${CC_RUNTIME_LANGUAGE} \
        --label ${CC_NAME}_${VERSION}
    echo "===================== Chaincode is packaged on peer0.org1 ===================== "
}

